ഹിന്ദുസ്ഥാൻ എന്നും ഇതു് അറിയപ്പെടുന്നുവെങ്കിലും ഈ പദം ഇന്ത്യൻ‍ യൂണിയനുപുറമെ പാകിസ്താനെയും ബംഗ്ലാദേശിനെയും കൂടി ഉൾക്കൊള്ളുന്നതാണ്.
പൂർണ്ണമായും ഭൂമിയുടെ ഉത്തരാർദ്ധ ഗോളത്തിലാണ്‌ ഇന്ത്യയുടെ സ്ഥാനം.
ദേശീയ പതാക
ഇന്ത്യയുടെ ദേശീയ പുഷ്പം താമരയും ദേശീയ വൃക്ഷം അരയാലുമാണ്.
ഉത്തരേന്ത്യയിൽ ഗോതമ്പ് കൃഷി ചെയ്യുമ്പോൾ ദക്ഷിണേന്ത്യയിലും തീരദേശങ്ങളിലും അരിയാണ്‌ പ്രധാനകൃഷി.
പതിനെട്ടാം നൂറ്റാണ്ടിന്റെ തുടക്കം മുതൽ ക്രമേണ ബ്രിട്ടീഷ് ഈസ്റ്റ് ഇന്ത്യാ കമ്പനി ഇന്ത്യയെ ഒരു ബ്രിട്ടീഷ് കോളനിയായി കയ്യടക്കി.
ഇങ്ങനെ തെറ്റായി ചിത്രീകരിക്കുന്നതിന്റെ കാരണങ്ങളെ ചില പശ്ചാത്യ പണ്ഡിതർ വിശകലനം ചെയ്തിട്ടുണ്ട്.
പുരുഷനിൽ സ്വയംഭോഗത്തിലൂടെയോ അല്ലാതെയോ സ്ഖലനം നടന്ന് ശുക്ലവിസർജ്ജനം നടക്കുമ്പോൾ പുതിയ ബീജകോശങ്ങളുണ്ടാവാനും ശുക്ലം പുതുതായി ഉല്പാദിപ്പിക്കാനുമുള്ള ശാരീരികപ്രേരണയുണ്ടാവുന്നു.
അദ്ദേഹത്തിന്റെ ശ്രമഫലമായി പാവങ്ങൾക്ക് സംരക്ഷണം നൽകാൻ ഹിൽഫ് അൽ ഫുദൂൽ സഖ്യം രൂപപ്പെട്ടു.
സ്വതന്ത്രമായി ഉപയോഗിക്കാനും ഉപയോഗക്രമത്തെക്കുറിച്ച് പഠിക്കാനും അതിൽ മാറ്റം വരുത്താനും യാതൊരു തടസ്സങ്ങളുമില്ലാതെ എത്ര പകർപ്പുകൾ  വേണമെങ്കിലും എടുത്ത് ഉപയോഗിക്കാനും സാധിക്കുന്ന സോഫ്റ്റ്‌വെയറുകളാണ് സ്വതന്ത്ര സോഫ്റ്റ്‌വെയർ.
ആസ്വാദകൻ തന്റെ അരങ്ങുപരിചയത്താൽ നടൻ കാണിക്കുന്നത്‌ സന്ദർഭാനുസരണം മനസ്സിലാകുന്നതാണ് നല്ലത്.
ശുക്ലം നഷ്ടപ്പെടുന്നത് ശരീരത്തെ തളർത്തുമെന്നും സ്വയംഭോഗം വന്ധ്യത വരുത്തിവയ്ക്കുമെന്നുമുള്ള വിശ്വാസം അപൂർവ്വമല്ല.
2008-ൽ ഇന്ത്യയുടെ ഓഹരി വ്യാപാരം ലോക ഓഹരി വ്യാപാരത്തിന്റെ 1.68 % ആയിത്തീർന്നു.
പിതാവിന്റെ പേര്: അബ്ദുല്ലാഹ് ബിൻ അബ്ദുൽ മുത്തലിബ് മാതാവിന്റെ പേര്: ആമിന ബിൻത് വഹബ്.
ലോകബാങ്കിന്റെ അന്തർദേശീയാടിസ്ഥാനത്തിൽ നിർ‌വ്വചിക്കപ്പെട്ടിട്ടുള്ള രാരിദ്രരേഖക്കു കീഴിലുള്ള തുകയായ ഒരു ദിവസത്തേക്ക് 1.25 ഡോളർ എന്ന തിഴിൽ വേതനം പറ്റുന്നവരുടെ ശതമാനം 1981-ലെ 60% എന്ന നിലയിൽ നിന്നു 2005-ൽ 42 % ആയിട്ടുണ്ട്.
