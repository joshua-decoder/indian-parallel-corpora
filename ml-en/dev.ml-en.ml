ഇതു കണക്കിലെടുത്ത് വന്ധ്യതാ ചികിത്സയിലെ ചില അവസരങ്ങളിൽ സ്വയംഭോഗം പ്രോത്സാഹിപ്പിക്കപ്പെടാറുണ്ട്.
