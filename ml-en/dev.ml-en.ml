ദേശീയ ഫലം മാങ്ങയാണ്‌ ദേശീയ ജലജീവിയായി 2009-ൽ സുസു എന്ന ശുദ്ധജല ഡോൾഫിനേയും തിരഞ്ഞെടുത്തു.
