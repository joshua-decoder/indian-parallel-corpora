സ്വാതന്ത്ര്യം കിട്ടിയതിനു ശേഷം മിക്കവാറും വർഷങ്ങൾ ഇന്ത്യ ഭരിച്ചത് ഇന്ത്യൻ നാഷണൽ കോൺഗ്രസ് നയിച്ച ഗവൺ‌മെന്റ് ആയിരുന്നു ഇന്ത്യൻ സംസ്ഥാനങ്ങളിലെ ദേശീയ പാർട്ടികളിൽ  ഇന്ത്യൻ നാഷണൽ കോൺഗ്രസ് ഭാരതീയ ജനതാ പാർട്ടി കമ്മ്യൂണിസ്റ്റ് പാർട്ടി ഓഫ് ഇന്ത്യ (മാർക്സിസ്റ്റ്) എന്നീ ദേശീയ പാർട്ടികളും മറ്റനേകം പ്രാദേശിക പാർട്ടികളും ഉൾപ്പെടുന്നു.1950-നും 1990-നും ഇടയിൽ രണ്ടു തവണയൊഴികെ ബാക്കി എല്ലാ തവണയും ഇന്ത്യൻ നാഷണൽ കോൺഗ്രസ് അധികാരത്തിലെത്തി.
മദീനയിൽ എത്തിയശേഷം പ്രവാചകൻ ആദ്യമായി ചെയ്തത് ഒരു കേന്ദ്രം നിർമ്മിക്കുക എന്നതായിരുന്നു.
ഇതേ സമയം തെന്നെ ഇൻഡോ-ഗ്രീക്ക് രജവംശം എത്തുന്നു.
അദ്ദേഹത്തിന്‌ മുമ്പും ദൂതൻമാർ കഴിഞ്ഞുപോയിട്ടുണ്ട്‌.
സിറിയയിലുള്ളവരുടെ മതജീവിതത്തെക്കുറിച്ച് വ്യക്തമായ കാഴ്ചപ്പാട് അദ്ദേഹം ഉണ്ടാക്കിയിരുന്നു.
ഭാവത്തിന്റെ സ്ഥാനത്ത് പദാർത്ഥത്തേയും രസത്തിന്റെ സ്ഥാനത്ത് വാക്യാർത്ഥത്തേയും സങ്കല്പിച്ച് ഭാവാശ്രയമായ നൃത്യത്തെ പദാർത്ഥാഭിനയപ്രധാനമെന്നും രസാശ്രയമായ നാട്യത്തെ വാക്യാർത്ഥാഭിനയപ്രാധാനമെന്നും വിശേഷിപ്പിക്കുന്നു.അതായത് വാച്യാർത്ഥത്തെ മുദ്രകളെക്കൊണ്ടും അവയ്ക്ക് ചേർന്ന ഭാവങ്ങൾകൊണ്ട് അഭിനയിക്കുമ്പോൾ അത് പദാർത്ഥത്തേയും ചെയ്യുന്നു.
