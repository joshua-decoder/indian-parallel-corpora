സാധാരണയായി സ്വതന്ത്രസോഫ്റ്റ്‌വെയർ സൌജന്യമായി ലഭ്യമാണ്.
മിക്ക കലാകാരന്മാരും പലരും മുദ്രകൾ ചുരുക്കി കാണിക്കാറുണ്ട്.
