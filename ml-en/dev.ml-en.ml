കുരിശുയുദ്ധങ്ങളുടെ സ്വാധീനം കാരണം ആദ്യകാലങ്ങളിലെ ചരിത്രങ്ങളിൽ മുഹമ്മദ് നബിയുടെ ജീവിതത്തെ തെറ്റായി ചിത്രീകരിക്കുന്ന പ്രവണത ശക്തമായിരുന്നു.
