ഇന്ത്യയിൽ കയറ്റുമതി ചെയ്യപ്പെടുന്ന പ്രധാന വസ്തുക്കളിൽ പെട്രോളിയം ഉല്പ്പന്നങ്ങൾ വസ്ത്രനിർമ്മാണ ഉല്പ്പന്നങ്ങൾ ജ്വല്ലറി വസ്തുക്കൾ സോഫ്റ്റ്‌വെയർ എഞ്ചിനീയറിങ്ങ് ഉപകരണങ്ങൾ കെമിക്കൽസ് തുകൽ അസംസ്കൃതവസ്തുക്കൾ എന്നിവയുൾപ്പെടുന്നു.
