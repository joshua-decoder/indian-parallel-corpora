തുടർന്ന് മഹാത്മാ ഗാന്ധിയുടെ നേതൃത്വത്തിൽ നടന്ന സമാധാനത്തിലൂന്നിയ സമരങ്ങളുടെ ഫലമായി 1947 ഓഗസ്റ്റ് 15നു ബ്രിട്ടീഷ്‌ കൊളോണിയൽ ഭരണത്തിൽ നിന്ന്‌ ഇന്ത്യ സ്വാതന്ത്ര്യം നേടി.
