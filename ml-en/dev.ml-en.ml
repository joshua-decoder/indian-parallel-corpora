വിവരണം
സംസ്കൃതത്തിൽ രചിക്കപ്പെട്ടവന്ദേമാതരം എന്ന ബങ്കിം ചന്ദ്ര ചാറ്റർജിയുടെ കൃതിയാണ്  ഇന്ത്യയുടെ ദേശീയ ഗീതം.
എന്നൽ ഭക്തിപ്രസ്ഥാനഫലമായി രൂപം കൊണ്ടത് പുരുഷപ്രധാനഭക്തിയാണ്.
സ്വന്തം കൈകൾ കൊണ്ട് നേരിട്ടോ അന്യവ്യക്തിയുടെ സഹായത്താലോ (ലൈംഗികവേഴ്ചയൊഴികെ) ഉപകരണങ്ങൾ ഉപയോഗിച്ചോ സ്വയം ഭോഗത്തിൽ ഉത്തേജനം സാധ്യമാക്കുന്നതും സ്വയംഭോഗത്തിൽ‌പ്പെടും.
1950 ജനുവരിയിലാണ് ഭരണഘടനാ സമിതി ഇതംഗീകരിച്ചത്.
പച്ച സൽക്കഥാപാത്രങ്ങളും (സാത്വികം) കത്തി രാജസ കഥാപാത്രങ്ങളും(രാജാക്കന്മാരായ ദുഷ്ടകഥാപാത്രങ്ങളും) ആണ്‌.
പദങ്ങൾ ചൊല്ലി ആടാൻ തുടങ്ങിയ കാലങ്ങളിൽ ആംഗികവാചികങ്ങളെ എങ്ങനെ പൊരുത്തപ്പെടുത്തും എന്ന സമസ്യയ്ക്ക് ഉത്തരമെന്ന നിലയിലാണ്  വെട്ടം കല്ലടിക്കോടൻ കപ്ലിങ്ങാടൻ സമ്പ്രദായങ്ങൾ ആവിർഭവിച്ചത്.
കമ്പ്യൂട്ടറുകൾ തമ്മിലുള്ള ബന്ധവും അവ തമ്മിൽ ആശയമോ ദത്തങ്ങളോ മറ്റു വിവരങ്ങളോ കൈമാറ്റം ചെയ്യുന്നത് സാധിച്ചെടുക്കുന്നതും നിയന്ത്രിക്കുന്നതും പ്രോട്ടോക്കോളുകളാണ്.
ഇളയ പുത്രി ഫാത്വിമ നബിയുടെ മരണത്തിന്‌ ശേഷമാണ്‌ മരണപ്പെട്ടത്.
